let fruit = "banana";

switch (fruit) {
    case "banana":
        console.log("Banana is good!");
        break;
    case "orange":
        console.log("I love oranges!");
        break;
    case "apple":
        console.log("I am not a fan of apples.");
        break;
    default:
        console.log("I have never heard of that fruit.");
}